/* ---------------- Engine ---------------- */
const Mobistro = (function () {
  const STORAGE_KEY = "mobistro_lang";
  const DEFAULT = "de";
  const codes = LANGS.map(l => l.code);

  function getLang() {
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      if (saved && codes.includes(saved)) return saved;
    } catch (e) {}
    const nav = (navigator.language || "").slice(0, 2).toLowerCase();
    return codes.includes(nav) ? nav : DEFAULT;
  }

  function t(key, lang) {
    const L = lang || getLang();
    return (I18N[L] && I18N[L][key] != null) ? I18N[L][key]
         : (I18N.en[key] != null ? I18N.en[key] : key);
  }

  function apply(lang) {
    const L = lang || getLang();
    const meta = LANGS.find(l => l.code === L) || LANGS[0];
    document.documentElement.lang = L;
    document.documentElement.dir = meta.rtl ? "rtl" : "ltr";

    document.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = t(el.getAttribute("data-i18n"), L);
    });
    document.querySelectorAll("[data-i18n-html]").forEach(el => {
      el.innerHTML = t(el.getAttribute("data-i18n-html"), L);
    });
    document.querySelectorAll("[data-i18n-attr]").forEach(el => {
      el.getAttribute("data-i18n-attr").split("|").forEach(pair => {
        const [attr, key] = pair.split(":");
        if (attr && key) el.setAttribute(attr.trim(), t(key.trim(), L));
      });
    });

    const page = document.body.getAttribute("data-page");
    if (page && I18N[L]["title." + page]) document.title = t("title." + page, L);

    document.querySelectorAll("[data-lang-current]").forEach(el => { el.textContent = meta.native; });
    document.querySelectorAll("[data-lang-opt]").forEach(btn => {
      btn.classList.toggle("sel", btn.getAttribute("data-lang-opt") === L);
    });
  }

  function setLang(lang) {
    const L = codes.includes(lang) ? lang : DEFAULT;
    try { localStorage.setItem(STORAGE_KEY, L); } catch (e) {}
    apply(L);
  }

  return { I18N, LANGS, getLang, setLang, apply, t };
})();

window.Mobistro = Mobistro;
